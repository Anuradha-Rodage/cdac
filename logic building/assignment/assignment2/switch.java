class switch{
 public static void main(String args[]){
      int val=5;
switch(val){
case 1:
     System.out.println("today is a monday");
 break;
case 2:
     System.out.println("today is tuesday");
break;

case 3:
    System.out.println("today is wednesday");
break;
default:
 System.out.println("invalid choice");
break;
}
}
}